#Exemplo de aplicação 3: Dada a lista de dados nums = [1, 4, 23, 11, 8], 
# corra a lista usando um objeto range e imprima cada elemento em uma linha.

nums = [1, 4, 23, 11, 8]
# A lista nums contém alguns números inteiros.
for i in range(len(nums)):# O laço for percorre cada índice da lista usando range(len(nums)),
    print(nums[i]) # e print(nums[i]) exibe cada elemento da lista em uma linha separada.

